def radix_sort_complex(lst):
    # Получаем максимальный модуль элемента списка
    max_mod = max(map(abs, lst))

    # Сортируем список элементов по действительной или мнимой части
    # a - список элементов, index - индекс части, которую мы сортируем
    def counting_sort(a, index):
        count = [0] * 256
        for x in a:
            value = int((x.real if index == 0 else x.imag) * 128 / max_mod) + 128
            count[value] += 1

        for i in range(1, 256):
            count[i] += count[i - 1]

        ans = [0] * len(a)
        for x in reversed(a):
            value = int((x.real if index == 0 else x.imag) * 128 / max_mod) + 128
            ans[count[value] - 1] = x
            count[value] -= 1

        return ans

    # Сортируем списки по действительной и мнимой частям
    for i in range(2):
        lst = counting_sort(lst, i)

    return lst

