def get_digit(num, digit):
    return (num // 10 ** digit) % 10

def msd_sort(nums, digits=None):
    if digits is None:
        digits = len(str(max(nums)))
    if len(nums) <= 1:
        return nums
    else:
        msd_lists = [[] for _ in range(10)]                         # создаем списки для каждой цифры
        for num in nums:
            msd = get_digit(num, digits)
            msd_lists[msd].append(num)                              # добавляем число в соответствующий список
        sorted_lists = [msd_sort(lst, digits-1) for lst in msd_lists if lst]  # сортируем списки рекурсивно
        return [num for lst in sorted_lists for num in lst]         # объединяем списки в один

